import { DEMO_ORG_ID, DEMO_OWNER_ID, daysAgo, daysFromNow } from './_shared';

/**
 * ~20 realistic Italian demo contacts owned across the demo team, used when
 * Supabase env is missing or a query fails. Varied status/source/tags/follow-up
 * windows so filters, sorting, the follow-up queue and bulk actions all have
 * meaningful data in "modalità demo".
 */

function c(
  partial: Pick<Contact, 'id' | 'first_name' | 'status' | 'source'> &
    Partial<Contact>,
): Contact {
  return {
    org_id: DEMO_ORG_ID,
    owner_marketer_id: DEMO_OWNER_ID,
    last_name: null,
    email: null,
    phone: null,
    city: null,
    tags: [],
    next_follow_up_at: null,
    last_interaction_at: null,
    notes: null,
    created_by: DEMO_OWNER_ID,
    updated_by: DEMO_OWNER_ID,
    created_at: daysAgo(40),
    updated_at: daysAgo(2),
    deleted_at: null,
    ...partial,
  };
}

